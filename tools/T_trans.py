import numpy as np

# 相机内参矩阵
K = np.array([[430.03326086956525, 0, 184.0583],
              [0, 430.03326086956525, 130.7467],
              [0, 0, 1]])

# 相机旋转矩阵
R = np.array([[1, 0, 0],
              [0, 1, 0],
              [0, 0, 1]])

# 相机坐标系下的平移向量（齐次坐标）
tx=-1.787557e+00
ty=1.361094e+00
tz=5.226973e+00
t_world = np.linalg.inv(K @ R) @ np.array([[tx], [ty], [tz], [1]], dtype=np.float32)

# 将相机坐标系下的平移向量转换为世界坐标系下的平移向量


# 世界坐标系下的平移向量
T = t_world[:3].flatten() / t_world[3]
print(T)